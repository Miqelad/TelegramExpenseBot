package com.paata.telegram_expense_bot.model.enums;

/**
 * Темы AI-анализа расходов.
 *
 * <p>Используются, чтобы сузить semantic search до категорий, которые важны
 * для конкретного вопроса пользователя.</p>
 */
public enum AnalysisTopic {

    /**
     * Анализ влияния расходов на здоровье.
     */
    HEALTH,

    /**
     * Анализ возможностей для экономии.
     */
    SAVINGS,

    /**
     * Анализ расходов на еду.
     */
    FOOD,

    /**
     * Анализ транспортных расходов.
     */
    TRANSPORT,

    /**
     * Анализ покупок.
     */
    SHOPPING,

    /**
     * Общий анализ без узкой темы.
     */
    GENERAL
}
