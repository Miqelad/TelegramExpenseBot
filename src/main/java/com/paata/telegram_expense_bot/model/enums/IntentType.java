package com.paata.telegram_expense_bot.model.enums;

/**
 * Типы пользовательских намерений, которые распознает LLM.
 */
public enum IntentType {

    /**
     * Пользователь хочет сохранить один или несколько расходов.
     */
    SAVE_EXPENSE,

    /**
     * Пользователь просит отчет по расходам за период.
     */
    MONTHLY_REPORT,

    /**
     * Пользователь просит отчет по конкретной категории или теме расходов.
     */
    CATEGORY_REPORT,

    /**
     * Пользователь просит AI-анализ, рекомендации или объяснение трат.
     */
    ANALYZE,

    /**
     * Намерение не удалось надежно распознать.
     */
    UNKNOWN
}
